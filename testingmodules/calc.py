class Calc(object):

    def __init__(self,a,b):
        self.a = a
        self.b = b

    def addition(self):
        return self.a + self.b
    def substraction(self):
        return self.a - self.b
    def multiplication(self):
        return self.a * self.b
    def dvision(self):
        return self.a / self.b
            